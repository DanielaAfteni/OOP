package com.company;

// PART 1
// Base abstract class Figure

public abstract class Figure {

    // with the following abstract methods - getArea()
    // which should return the area of some figure
    abstract double getArea();

    // with the following abstract methods - getPerimeter()
    // which should return the perimeter of some figure
    abstract double getPerimeter();

    // extra String method for printing the corresponding name of the figure
    abstract String getName();
}
